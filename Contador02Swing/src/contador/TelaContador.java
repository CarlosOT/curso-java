    private void btnContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContarActionPerformed
        // TODO add your handling code here:
        int cc = 0;
        int nMax = 30;
        int nMin = 1;
        //int intervalo = (nMax - nMin) + nMin;
        int nAleatorio = (int)((Math.random() * nMax) + nMin);
        String contagem = "";
        while (cc < nAleatorio){
            cc++;
            if (cc == 1 || cc == 2){
                continue;
            }
            if (cc == 29){
                break;
            }
            contagem += cc + " ";
        }
        lblContagem.setText(contagem);
        lblAleatorio.setText(Integer.toString(nAleatorio));
    }//GEN-LAST:event_btnContarActionPerformed

